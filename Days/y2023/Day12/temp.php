function checkPattern(int $char, int $step, int $left): int
{
    global $pattern;
    global $sequence;
    if ($char === strlen($pattern)) {
        if ($step === count($sequence) && $left === 0) {
            return 1;
        }
        return 0;
    }
    if ($step > count($sequence)) {
        return 0;
    }
    $total = 0;
    if (substr($pattern, $char, 1) === '.') {
        if ($left === 0) {
            if ($step < count($sequence)) {
                $total += checkPattern($char + 1, $step + 1, $sequence[$step]);
            }
            $total += checkPattern($char + 1, $step, 0);
        }
    } elseif (substr($pattern, $char, 1) === '#') {
        if ($left > 0) {
            $total += checkPattern($char + 1, $step, $left - 1);
        }
    } else {
        if ($left === 0) {
            if ($step < count($sequence)) {
                $total += checkPattern($char + 1, $step + 1, $sequence[$step]);
            }
            $total += checkPattern($char + 1, $step, 0);
        }
        if ($left > 0) {
            $total += checkPattern($char + 1, $step, $left - 1);
        }
    }
    return $total;
}

